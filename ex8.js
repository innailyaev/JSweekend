//Ex 8 - Find the Perimeter of a Rectangle

const findPerimeter=(length,width)=>{
    return 2*length + 2*width;
}

console.log("The perimeter of a rectangle is:",findPerimeter(6,7));
console.log("The perimeter of a rectangle is:",findPerimeter(20,10));
console.log("The perimeter of a rectangle is:",findPerimeter(2,9));